
//wrappers provide a way to use primitive data types as objects
//Primitive Data Type 	Wrapper Class
//byte 	Byte
//short 	Short
//int 	Integer
//long 	Long
//float 	Float
//double 	Double
//boolean 	Boolean
//char 	Character

// use wrapper classes when working with collection objects (Arraylist)
public class wrappers {
    public static void main (String[] args){
            // to create wrapper objects ===> use Wrapper class
        Integer myInt = 5;
        Double myDouble = 10.56;
        Character myChar = 'H';

        System.out.println(myInt);
        System.out.println(myDouble);
        System.out.println(myChar);

    }
}
